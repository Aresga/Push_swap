#include "../inc/ft_printf.h"

int	ft_putnbr_u(unsigned int n)
{
	int	i;

	i = 0;
	if (n >= 10)
		i += ft_putnbr_u(n / 10);
	i += ft_putchar((n % 10) + '0');
	return (i);
}
