#include "../inc/libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*trimresult;
	size_t	start;
	size_t	end;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (start < end && ft_strchr(set, s1[end - 1]))
		end--;
	len = end - start;
	trimresult = (char *)malloc(sizeof(char) * (len + 1));
	if (!trimresult)
		return (NULL);
	ft_strlcpy(trimresult, s1 + start, len + 1);
	return (trimresult);
}
